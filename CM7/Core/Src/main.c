static void uart1_tx_dma(const uint8_t *data, uint16_t len)
{
  /* Demo-grade: wait until previous TX DMA finished */
  while (huart1.gState != HAL_UART_STATE_READY) { }
  HAL_UART_Transmit_DMA(&huart1, (uint8_t*)data, len);
}

